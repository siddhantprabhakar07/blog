<img class="thumbnail" src="image/mushroom_cultivation.jpg" style="width: 100%;">

<p>Need of the
activity: Ghatshila adjacent to Jamshedpur having connectivity with National Highway
and Railways has always faced distressed migration due to lack of
employment opportunities. Rain fed climate accentuated the condition
towards more than worse. Mono cropping culture and lack of alternative
livelihood opportunities led to low annual income. There was the need to
create or identify alternative employment opportunities which could withstand
the harsh climate and ensure a good return on investment.</p>
<p>Intervention: In order to generate alternative employment opportunities, Gram Sabha and
Choupal decided to harness the know-how of mushroom cultivation aiming
to incorporate the strong network of SHGs.
During the inception phase, the community was reached out through the
meetings of the SHGs, Choupal etc. which helped in bringing the
likeminded people on the same platform thus created a network of people
with the common interest. These people were further motivated to ensure
their participation and the engagement of their extended portion of the land
resource. The assurance of finance, technical guidance including field
demonstration, exposure and training has made these farmers to adopt this
project.
The Promotion of Mushroom Cultivation in Dharambahal Cluster is being
implemented in five villages namely Kalapathar, Mahalidih, UparPaora,
Chengjora of Kashida Gram Panchayat and Edalbera of Dharambahal Gram
Panchayat. 46 women farmers linked with SHGs are part of this project who
are the primary beneficiaries.
    
</p>
<p>Impact: Change in perception towards the project could be felt in the cluster. The
households, which were not part of this project, have expressed their interest
for taking up the mushroom cultivation because of the ease of doing and
benefits associated with it. Now, the beneficiaries like Parbati Murmu
(Village- UparPaora Marsal Mahila Swayam Sahayata Samuh) and
DumniBesra (Village- UparPaora Marsal Mahila Swayam SahayataSamuh)
do not migrate because of their engagement in the mushroom cultivation.
Due to income generation through Mushroom Cultivation, these women
have started supporting their families and migration rate has come down

drastically. A sense of dignity has been restored into them.
    
</p>

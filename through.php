<h3>Creating Livelihoods through Skill Training <small>3/6/2015</small></h3>

<div class="callout">
<ul class="menu simple">
<li><a href="#">Cluster Details:</a></li>
<li><a href="#">Cluster: Nandalaur</a></li>
<li><a href="#">District: YSR Kadapa</a></li>
<li><a href="#">SPMRM phase: II</a></li>
<li><a href="#">Category: Non Tribal</a></li>
<li><a href="#">State: Andhra Pradesh</a></li>
</ul>
</div>

<img class="thumbnail" src="image/creating_liveihoods.jpg" style="width: 100%;">

<p>Need of the
activity: Due to poor economic background of cluster Nandalaur need of skill
training linked by economic activity came in existence. Importance of
skill development trainings was felt necessary for better avenues to rural
youth in wage employment as well as self-employment creation.
    
</p>
<p>
    Intervention: The non-residential training program to 156 trainees under DDU-GKY
    norms was imparted in the field of beautician, sewing machine operator,
    computer assistant and light motor vehicle driving courses in year 2018-
    19.These youth completed training as per the DDU-GKY standards.
    To give further impetus to skill development trainings in Nandalur, a Skill
    Development Training Centre was sanctioned with Rs 60 lakh SPMRM
    funds and the construction is underway at the cluster headquarters. The
    building, once completed, will cater to the needs of unemployed youth of
    nearby areas and is expected to help them in imparting trainings in
    different field to secure a better livelihood.
</p>
<p>Impact: Skill training becomes path for getting gainful employment opportunities
at different places of the State. The training to youth of this region turned
out to be a boon for them. Now having found placements, they are starting
an exciting phase in their careers.
    
</p>
<p>Key
Stakeholders: DDU-GKY team, Concerned PIAs, Gram Panchayats (for mobilisation of
youth)
    
</p>
<p>Beneficiaries: Youth of the cluster
    
</p>
<p>Activity
Details: Component: Skill Training linked to economic activities
Sub Component: Skill Training linked to Wage employment
Total Investment: Rs.2.30 crore
CGF invested:Rs.1.30 crore
Convergence invested: Rs.1.00 crore
Implementing agency: NRLM
O&amp;M Agency: Rural Works Department
</p>
